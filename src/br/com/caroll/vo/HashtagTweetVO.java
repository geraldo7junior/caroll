package br.com.caroll.vo;

public class HashtagTweetVO {
	
	private int hashtagFk;
	private int tweetFk;
	
	public int getHashtagFk() {
		return hashtagFk;
	}
	public void setHashtagFk(int hashtagFk) {
		this.hashtagFk = hashtagFk;
	}
	public int getTweetFk() {
		return tweetFk;
	}
	public void setTweetFk(int tweetFk) {
		this.tweetFk = tweetFk;
	}
	
	
	
}
