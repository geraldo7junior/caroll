package br.com.caroll.vo;

public class HashtagVO {
	private int idHashtag;
	private String dadoHashtag;
	
	
	public int getIdHashtag() {
		return idHashtag;
	}
	public void setIdHashtag(int idHashtag) {
		this.idHashtag = idHashtag;
	}
	public String getDadoHashtag() {
		return dadoHashtag;
	}
	public void setDadoHashtag(String dadoHashtag) {
		this.dadoHashtag = dadoHashtag;
	}
	
	

}
